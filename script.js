const profileBio = document.getElementById("profile-bio");
const statYears = document.getElementById("stat-years");
const statRepos = document.getElementById("stat-repos");
const statFollowers = document.getElementById("stat-followers");
const githubLink = document.getElementById("github-link");
const projectCards = Array.from(document.querySelectorAll("[data-project-card]"));

if (yearNode) {
  yearNode.textContent = String(new Date().getFullYear());
}

// Translation system
const getTranslation = (key) => {
  const keys = key.split(".");
  let value = translations[currentLanguage];
  for (const k of keys) {
    value = value[k];
    if (!value) return key;
  }
  return value;
};

const applyTranslations = () => {
  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.getAttribute("data-i18n");
    const translation = getTranslation(key);
    element.textContent = translation;
  });

  // Update form labels properly
  document.querySelectorAll("label[data-i18n]").forEach((element) => {
    const key = element.getAttribute("data-i18n");
    const translation = getTranslation(key);
    const input = element.querySelector("input, textarea");
    if (input) {
      element.textContent = translation;
      element.appendChild(input);
    } else {
      element.textContent = translation;
    }
  });

  // Update form placeholders
  const nameInput = document.querySelector("input[name='name']");
  if (nameInput) nameInput.placeholder = getTranslation("placeholders.name");
  
  const emailInput = document.querySelector("input[name='email']");
  if (emailInput) emailInput.placeholder = getTranslation("placeholders.email");
  
  const messageTextarea = document.querySelector("textarea[name='message']");
  if (messageTextarea) messageTextarea.placeholder = getTranslation("placeholders.message");
};

const setLanguage = (lang) => {
  currentLanguage = lang;
  localStorage.setItem("language", lang);
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";
  applyTranslations();

  // Update language toggle button styles
  if (langToggle) {
    langToggle.classList.toggle("ar-active", lang === "ar");
  }
};

if (langToggle) {
  langToggle.addEventListener("click", () => {
    setLanguage(currentLanguage === "en" ? "ar" : "en");
  });
}

// Set initial language
setLanguage(currentLanguage);

if (menuToggle && nav) {
  menuToggle.addEventListener("click", () => {
    const expanded = menuToggle.getAttribute("aria-expanded") === "true";
    menuToggle.setAttribute("aria-expanded", String(!expanded));
    nav.classList.toggle("open");
  });

  navLinks.forEach((link) => {
    link.addEventListener("click", () => {
      nav.classList.remove("open");
      menuToggle.setAttribute("aria-expanded", "false");
    });
  });
}

